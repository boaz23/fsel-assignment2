package hw2.data;

public class Hall {
    private String hallName;
    private int seatsAmount;

    public Hall(String hallName, int seatsAmount) {
        this.hallName = hallName;
        this.seatsAmount = seatsAmount;
    }

    public String getHallName() {
        return hallName;
    }

    public int getSeatsAmount() {
        return seatsAmount;
    }


}
